    void agregar()
    {
        if(txtNombre.getText().equals(""))
        {
            txtNombre.requestFocus();
            txtNombre.setBackground(Color.yellow);
            
            txtCp.setBackground(Color.white);
            txtCuil.setBackground(Color.white);
            txtDireccion.setBackground(Color.white);
            txtTelefono.setBackground(Color.white);
            
            JOptionPane.showMessageDialog(this, "Debe ingresar un NOMBRE");
        }
        else
        {
            if(txtCp.getText().equals(""))
            {
                txtCp.requestFocus();
                txtCp.setBackground(Color.yellow);
                
                txtNombre.setBackground(Color.white);
                txtCuil.setBackground(Color.white);
                txtDireccion.setBackground(Color.white);
                txtTelefono.setBackground(Color.white);
                
                JOptionPane.showMessageDialog(this, "Debe ingresar un CODIGO POSTAL");
            }
            else
            {
                if(txtCuil.getText().equals(""))
                {
                    txtCuil.requestFocus();
                    txtCuil.setBackground(Color.yellow);
                    
                    txtNombre.setBackground(Color.white);
                    txtCp.setBackground(Color.white);
                    txtDireccion.setBackground(Color.white);
                    txtTelefono.setBackground(Color.white);
                    
                    JOptionPane.showMessageDialog(this, "Debe ingresar un CUIL/CUIT");
                }
                else
                {
                    if(!validarNumeros(txtCp.getText().trim()))
                            {
                                txtCp.requestFocus();
                                txtCp.setBackground(Color.yellow);
                                
                                txtRazon.setBackground(Color.white);
                                txtTelefono.setBackground(Color.white);
                                txtCuil.setBackground(Color.white);
                                txtDireccion.setBackground(Color.white);
                                txtNombre.setBackground(Color.white);
                                
                                JOptionPane.showMessageDialog(null, "Debe ingresar solo numeros en el campo CODIGO POSTAL");
                            }
                            else
                            {
                                if(!validarNumeros(txtCuil.getText().trim()))
                                {
                                    txtCuil.requestFocus();
                                    txtCuil.setBackground(Color.yellow);
                                    
                                    txtRazon.setBackground(Color.white);
                                    txtTelefono.setBackground(Color.white);
                                    txtCp.setBackground(Color.white);
                                    txtDireccion.setBackground(Color.white);
                                    txtNombre.setBackground(Color.white);

                                    JOptionPane.showMessageDialog(null, "Debe ingresar solo numeros en el campo CUIL/CUIT");
                                }
                                else
                                {
                                    if(!validarNumeros(txtTelefono.getText().trim()))
                                    {
                                        txtTelefono.requestFocus();
                                        txtTelefono.setBackground(Color.yellow);
                                        
                                        txtRazon.setBackground(Color.white);
                                        txtCuil.setBackground(Color.white);
                                        txtCp.setBackground(Color.white);
                                        txtDireccion.setBackground(Color.white);
                                        txtNombre.setBackground(Color.white);
                                        
                                        JOptionPane.showMessageDialog(null, "Debe ingresar solo numeros en el campo TELEFONO");
                                    }
                                    else
                                    {
                                        agregarCliente();
                            
                                        txtNombre.requestFocus();
                                        txtNombre.setBackground(Color.yellow);

                                        txtCp.setBackground(Color.white);
                                        txtCuil.setBackground(Color.white);
                                        txtDireccion.setBackground(Color.white);
                                        txtTelefono.setBackground(Color.white);
                                        txtRazon.setBackground(Color.white);

                                        JOptionPane.showMessageDialog(this, "CLIENTE CARGADO EXITOSAMENTE");
                                    }
                                }
                            }
                }
            }
        }
        
    }
    
