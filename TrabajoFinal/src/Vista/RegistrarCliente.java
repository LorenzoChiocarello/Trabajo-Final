    void agregarCliente()
    {
        try
        {
            PreparedStatement guardar = conect.prepareStatement("INSERT INTO clientes (nombre, id_razon_social, cp,cuil, direccion, telefono, estado) VALUES (?, ?, ?, ?, ?,?, 1)");
            guardar.setString(1, txtNombre.getText());
            guardar.setString(2, txtRazon.getText());
            guardar.setString(3, txtCp.getText());
            guardar.setString(4, txtCuil.getText());
            guardar.setString(5, txtDireccion.getText());
            guardar.setString(6, txtTelefono.getText());
            
            guardar.executeUpdate();
            
            //JOptionPane.showMessageDialog(null, "Cliente cargado exitosamente");
            
            txtNombre.setText("");
            txtCp.setText("");
            txtCuil.setText("");
            txtDireccion.setText("");
            txtTelefono.setText("");
            txtRazon.setText("");
            txtNombre.requestFocus();
            
            
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, e + "No se pudo cargar");
        }
    }

