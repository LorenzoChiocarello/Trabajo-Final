    public void mostrarDatosUsuario(int opbuscar, String valor)
    {
        DefaultTableModel tusuario = new DefaultTableModel();
        
        tusuario.addColumn("IDENTIFICACION");
        tusuario.addColumn("USUARIO");
        tusuario.addColumn("PASSWORD");
        
        lstTablaUsuario.setModel(tusuario);
        
        String codsql;
        
        if(opbuscar == 0 && valor == null)
        {
            codsql = "SELECT id, usuario, pass FROM usuario where estado=1";
        }
        else
        {
            if(opbuscar == 1 && valor != null)
            {
                codsql = "SELECT id, usuario, pass FROM usuario WHERE usuario LIKE '%"+valor+"%' and estado=1";
            }
            else
            {
                codsql= "SELECT id, usuario, pass FROM usuario where estado=1";
            } 
        }
        
        String []datos = new String[10];
        
        try
        {
            Statement leer = conect.createStatement();
            ResultSet resultado = leer.executeQuery(codsql);
                
            while(resultado.next())
            {
                datos[0] = resultado.getString(1);
                datos[1] = resultado.getString(2);
                datos[2] = resultado.getString(3);
                    
                tusuario.addRow(datos);
            }
            
            lstTablaUsuario.setModel(tusuario);
            
            
            
        }
        catch (SQLException e) 
        {
            JOptionPane.showMessageDialog(null, e +  "Error en la consulta");
        }
    }
    
    public void eliminarUsuario()
    {
        int fila = lstTablaUsuario.getSelectedRow();
        
        if(fila>=0)
        {
            try 
            {
                String valor = lstTablaUsuario.getValueAt(fila, 0).toString();
            
                PreparedStatement eliminar = conect.prepareStatement("UPDATE usuario SET estado=2 FROM WHERE id='"+valor+"'");
                eliminar.executeUpdate();
                mostrarDatosUsuario(0,null);
            } 
            catch (Exception e) 
            {
                JOptionPane.showMessageDialog(null,e + "No se pudo eliminar");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "No selecciono ninguno");
        }
        
    }

