    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(lstTablaUsuario.getSelectedRow()>=0)
        {
            try
            {
                ModificarUsuario lrs= new ModificarUsuario();
                Principal.VentanaPrincipal.add(lrs);
                lrs.toFront();
                lrs.setVisible(true);

                Dimension desktopSize = VentanaPrincipal.getSize();
                Dimension FrameSize = lrs.getSize();
                lrs.setLocation((desktopSize.width - FrameSize.width)/2, (desktopSize.height- FrameSize.height)/2);
                lrs.show();
                
                DefaultTableModel tm = (DefaultTableModel)lstTablaUsuario.getModel();
                int id = Integer.parseInt(String.valueOf(tm.getValueAt(lstTablaUsuario.getSelectedRow(), 0)));
                String usuario = tm.getValueAt(lstTablaUsuario.getSelectedRow(),1).toString();
                String pass = tm.getValueAt(lstTablaUsuario.getSelectedRow(),1).toString();
                
                ModificarUsuario.txtId.setText(String.valueOf(id));
                ModificarUsuario.txtUsuario.setText(usuario);
                ModificarUsuario.txtPass.setText(pass);
                
                
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this, "NO CONTIENE UN USUARIO");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "DEBE SELECCIONAR UN USUARIO");
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed


