    public void mostrarDatosVendedor(int opbuscar, String valor)
    {
        DefaultTableModel tVendedor = new DefaultTableModel();
        
        tVendedor.addColumn("IDENTIFICACION");
        tVendedor.addColumn("NOMBRE");
        tVendedor.addColumn("CODIGO POSTAL");
        tVendedor.addColumn("DIRECCION");
        tVendedor.addColumn("TELEFONO");
        
        lstVendedor.setModel(tVendedor);
        
        String codsql;
        
        if(opbuscar == 0 && valor == null)
        {
            codsql = "SELECT id, nombre, cp, direccion, telefono FROM vendedores where estado=1";
        }
        else
        {
            if(opbuscar == 1 && valor != null)
            {
                codsql = "SELECT id, nombre, cp, direccion, telefono FROM vendedores WHERE nombre LIKE '%"+valor+"%' and estado=1";
            }
            else
            {
                codsql = "SELECT id, nombre, cp, direccion, telefono FROM vendedores where estado=1";
            }
        }
        
        String []datos = new String[10];
        
        try
        {
            Statement leer = conect.createStatement();
            ResultSet resultado = leer.executeQuery(codsql);
                
            while(resultado.next())
            {
                datos[0] = resultado.getString(1);
                datos[1] = resultado.getString(2);
                datos[2] = resultado.getString(3);
                datos[3] = resultado.getString(4);
                datos[4] = resultado.getString(5);

                    
                tVendedor.addRow(datos);
            }
            
            lstVendedor.setModel(tVendedor);
            
            
            
        }
        catch (SQLException e) 
        {
            JOptionPane.showMessageDialog(null, e +  "Error en la consulta");
        }
    }

