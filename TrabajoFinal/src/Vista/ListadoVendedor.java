    public void eliminarVendedor()
    {
        int fila = lstVendedor.getSelectedRow();
        
        if(fila>=0)
        {
            try 
            {
                String valor = lstVendedor.getValueAt(fila, 0).toString();

                PreparedStatement eliminar = conect.prepareStatement("UPDATE vendedores SET estado=2 WHERE id='"+valor+"'");
                eliminar.executeUpdate();
                mostrarDatosVendedor(0,null);
            } 
            catch (Exception e) 
            {
                JOptionPane.showMessageDialog(null,e + "No se pudo eliminar");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "No selecciono ninguno");
        }
        
    }
    
