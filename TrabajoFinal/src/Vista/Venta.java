    void calcularTotal()
    {
        tpagar=0;
        for (int i = 0; i < TablaDetalle.getRowCount(); i++) {
            int cantidad=Integer.parseInt(TablaDetalle.getValueAt(i, 3).toString());
            double precio=Double.parseDouble(TablaDetalle.getValueAt(i, 4).toString());
            
            tpagar=tpagar+(cantidad*precio);
        }
        
        txtTotal.setText(""+tpagar);
    }
    
    void actualizarStock()
    {
        for (int i = 0; i < modelo.getRowCount(); i++) {
            Articulo a=new Articulo();
            idp=Integer.parseInt(TablaDetalle.getValueAt(i, 1).toString());
            int cant=Integer.parseInt(TablaDetalle.getValueAt(i, 3).toString());
            
            a=adao.listarID(idp);
            int sa=a.getStock()-cant;
            adao.actualizarStock(sa,idp);
        }
    }
    
    void guardarVenta()
    {
        int idc=Integer.parseInt(txtCodCliente.getText());
        int idv=Integer.parseInt(txtVendedor.getText());
        int formapago=Integer.parseInt(txtPago.getText());
        String serie = txtSerie.getText();
        double monto = tpagar;
        String comprobante = cboComprobante.getSelectedItem().toString();
        Date date = jdcFecha.getDate();
                
        long d = date.getTime();
        java.sql.Date fecha = new java.sql.Date(d);
        
        v.setIdCliente(idc);
        v.setIdVendedor(idv);
        v.setId_formapago(formapago);
        v.setMonto(monto);
        v.setNumeroSerie(serie);
        v.setComprobante(comprobante);
        v.setFecha(fecha);
        
        vdao.GuardarVentas(v);
    }
    
    void guardarDetalle() throws SQLException
    {
        String idv=vdao.IdVentas();
        int idve=Integer.parseInt(idv);
        for (int i = 0; i < TablaDetalle.getRowCount(); i++) {
            int idp=Integer.parseInt(TablaDetalle.getValueAt(i, 1).toString());
            int cantidad=Integer.parseInt(TablaDetalle.getValueAt(i, 3).toString());
            double importe=Double.parseDouble(TablaDetalle.getValueAt(i, 5).toString());
            
            dv.setIdArticulo(idp);
            dv.setIdVenta(idve);
            dv.setCantidad(cantidad);
            dv.setImporte(importe);
            
            vdao.GuardarDetalleVentas(dv);
        }
        
    }

