    void agregarProducto()
    {
        double total=0;
        int item = 0;
        modelo=(DefaultTableModel)TablaDetalle.getModel();
        item=item+1;
        //idp=a.getCodigo();
        idp=Integer.parseInt(txtCodProducto.getText());
        String nomp=txtProducto.getText();
        double precio=Double.parseDouble(txtPrecio.getText());
        int cantidad=Integer.parseInt(txtCantidad.getText());
        int stock=Integer.parseInt(txtStock.getText());
        
        total=cantidad*precio;
        
        ArrayList lista = new ArrayList();
        if(stock>cantidad)
        {
            lista.add(item);
            lista.add(idp);
            lista.add(nomp);
            lista.add(cantidad);
            lista.add(precio);
            lista.add(total);
            Object[]ob=new Object[6];
            ob[0]=lista.get(0);
            ob[1]=lista.get(1);
            ob[2]=lista.get(2);
            ob[3]=lista.get(3);
            ob[4]=lista.get(4);
            ob[5]=lista.get(5);
            
            modelo.addRow(ob);
            TablaDetalle.setModel(modelo);
            
            calcularTotal();
            
            txtCodProducto.requestFocus();
            txtCodProducto.setBackground(Color.yellow);
            txtCantidad.setBackground(Color.white);
            
            DefaultTableCellRenderer tcr = new DefaultTableCellRenderer();
            tcr.setHorizontalAlignment(SwingConstants.RIGHT);
            TablaDetalle.getColumnModel().getColumn(4).setCellRenderer(tcr);
            TablaDetalle.getColumnModel().getColumn(5).setCellRenderer(tcr);

        }
        else
        {
            JOptionPane.showMessageDialog(this, "Stock Insuficiente");
            
            txtCantidad.requestFocus();
        }
        
    }
    
