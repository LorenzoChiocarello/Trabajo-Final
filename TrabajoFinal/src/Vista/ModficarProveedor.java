    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if(txtNombre.getText().equals(""))
       {
           txtNombre.requestFocus();
           txtNombre.setBackground(Color.yellow);
           
           txtCp.setBackground(Color.white);
           txtDireccion.setBackground(Color.white);
           txtTelefono.setBackground(Color.white);
           txtEmail.setBackground(Color.white);
           
           JOptionPane.showMessageDialog(null, "Debe ingresar el nombre del proveedor");
       }
        else
        {
           if(txtCp.getText().equals(""))
           {
               txtCp.requestFocus();
               txtCp.setBackground(Color.yellow);
               
               txtNombre.setBackground(Color.white);
               txtDireccion.setBackground(Color.white);
               txtTelefono.setBackground(Color.white);
               txtEmail.setBackground(Color.white);
               
               JOptionPane.showMessageDialog(null, "Debe ingresar el codigo postal del proveedor");
           }
           else
           {
               if(txtTelefono.getText().equals(""))
                {
                       txtTelefono.requestFocus();
                       txtTelefono.setBackground(Color.yellow);
                       
                       txtCp.setBackground(Color.white);
                       txtDireccion.setBackground(Color.white);
                       txtNombre.setBackground(Color.white);
                       txtEmail.setBackground(Color.white);
                       
                       JOptionPane.showMessageDialog(null, "Debe ingresar el telefono del proveedor");
                }
               else
               {
                   if(!validarNumeros(txtTelefono.getText().trim()))
                    {
                               txtTelefono.requestFocus();
                               txtTelefono.setBackground(Color.yellow);
                               
                               txtCp.setBackground(Color.white);
                                txtDireccion.setBackground(Color.white);
                                txtNombre.setBackground(Color.white);
                                txtEmail.setBackground(Color.white);

                                JOptionPane.showMessageDialog(null, "Debe ingresar solo numeros en el campo TELEFONO");
                               
                               
                    }
                   else
                   {
                       if(!validarNumeros(txtCp.getText().trim()))
                        {
                                   txtCp.requestFocus();
                                   txtCp.setBackground(Color.yellow);
                                   
                                   txtNombre.setBackground(Color.white);
                                    txtDireccion.setBackground(Color.white);
                                    txtTelefono.setBackground(Color.white);
                                    txtEmail.setBackground(Color.white);
                                   
                                   JOptionPane.showMessageDialog(null, "Debe ingresar solo numeros en el campo CODIGO POSTAL");
                        }
                       else
                       {
                           int r = showConfirmDialog(null, "DESEA MODIFICAR EL PROVEEDOR?");
                           
                           if(r == 0)
                           {
                               actualizarProveedor();
                           
                                    JOptionPane.showMessageDialog(null, "PROVEEDOR CARGADO EXITOSAMENTE");

                                    txtNombre.requestFocus();
                                    txtNombre.setBackground(Color.yellow);

                                    txtCp.setBackground(Color.white);
                                    txtDireccion.setBackground(Color.white);
                                    txtTelefono.setBackground(Color.white);
                                    txtEmail.setBackground(Color.white);
                                    
                                    dispose();
                           }
                           else
                           {
                               
                           }
                           
                       }
                   }
               }
           }
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

