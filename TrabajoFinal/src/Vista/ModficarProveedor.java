    void actualizarProveedor()
    {
        try 
        {
            PreparedStatement pst = conect.prepareStatement("UPDATE proveedores SET nombre='"+txtNombre.getText()+"',cp='"+txtCp.getText()+"',direccion='"+txtDireccion.getText()+"',telefono='"+txtTelefono.getText()+"',email='"+txtEmail.getText()+"' WHERE id='"+txtId.getText()+"'");
            pst.executeUpdate();
            //mostrarDatosProveedor(0, null);
        }
        catch (Exception e) 
        {
            System.out.println(e.getMessage());
        }
    }
    
